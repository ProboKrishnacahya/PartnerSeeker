        <h1>Apa itu Portofolio? Bagaimana Cara Membuatnya?</h1><br><br>
        <article>Portofolio adalah dokumen laporan lengkap berisi tentang profil biodata dan penggambaran diri terhadap dunia karier yang dijalani (dapat berupa hasil karya proyek dan sertifikat serta hal berkualitas lainnya) dengan disusun sedemikian rupa sehingga nampak rapi dan menarik. Tujuannya yaitu untuk mendokumentasikan perkembangan perjalanan karier dalam rangka menambah progresnya. Dengan melampirkan capaian diri, maka pihak perekrut dapat memiliki acuan yang lebih terukur terhadap pembuat portofolio tersebut.<br><br>

            Selain menuliskan hal-hal yang wajib seperti nama dan skill, kita juga bisa menuliskan pencapaian terbesar, goals di masa depan, hobi, jurusan pendidikan, dan lain-lainnya. Ceritakanlah sesuatu yang membuat klien mengenal Anda tanpa perlu menghubungi Anda terlebih dahulu.<br><br>

            Selanjutnya, Anda cukup menampilkan beberapa daftar proyek yang menurut Anda terbaik dan tentunya relevan dengan bidang yang Anda tuju meskipun hanya berjumlah empat atau lima proyek karena selain memudahkan dan mempercepat klien dalam melihat hasil kerja Anda, portofolio bukanlah sebagai ajang pamer semua hal yang telah dikerjakan.<br><br>

            Kemudian, untuk memperindah tampilan portofolio, Anda dapat menggunakan stock images jika Anda memang kurang memiliki keahlian untuk menghasilkan gambar yang menarik. Di internet telah tersebar banyak website yang menyediakan stock images secara gratis maupun berbayar, contohnya seperti <a href="https://unsplash.com/">Unsplash</a>, <a href="https://pixabay.com/id/">Pixabay</a>, <a href="https://www.vecteezy.com/">Vecteezy</a>, <a href="https://www.freepik.com/">Freepik</a>, <a href="https://www.pexels.com/id-id/">Pexels</a>, dan lain-lainnya. Pilihlah gambar yang cocok untuk mewakili atau memperkuat ilustrasi terhadap penjelasan proyek Anda. Selain itu, bisa juga ditambahkan beberapa ikon dengan warna yang seragam dan ukuran yang dapat terlihat dengan jelas pada subtopik isi portofolio Anda.<br><br>

