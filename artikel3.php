        <h1>Perbedaan Freelance, Part Time Job, Full Time Job, dan Internship</h1><br><br>
        <article>Terdapat beberapa jenis pekerjaan yang banyak ditemui di dunia karier. Masing-masing jenis pekerjaan tetaplah memiliki kelebihan dan kekurangannya sendiri. Simak komparasinya dibawah ini<br><br>

            Freelancer (Pekerja Lepas) adalah pekerja yang tak terikat dengan perusahaan manapun dalam rentang waktu yang panjang. Perusahaan yang menggunakan jasanya akan menentukan kesepakatan proyek yang akan dibuat dan batas waktu proses pengerjaan proyek tersebut. Hal penting untuk menjadi freelancer yaitu perlu memiliki keahlian khusus, pengalaman, reputasi, dan tentunya relasi.<br><br>

            Kelebihannya:<br>
            <li>bisa mengerjakan proyek yang sesuai dengan passion dan skill</li>
            <li>bisa memasang tarif sesuai keinginan</li>
            <li>bisa bekerja di lokasi manapun dengan waktu yang lebih fleksibel</li>
            <li>tidak terikat dengan peraturan perusahaan</li><br>

            Kekurangannya:<br>
            <li>tidak terjamin memperoleh penghasilan yang tetap karena tidak ada kepastian mendapatkan proyek dalam waktu berdekatan</li>
            <li>biasanya bekerja lebih lama dibandingkan karyawan full time karena mengerjakan permintaan klien secara individual</li>
            <li>umumnya tidak mendapat akses untuk menggunakan fasilitas perusahaan</li><br><br>

            Full Time Job Employee (Pekerja Purnawaktu) adalah karyawan yang bekerja dengan jam waktu penuh sesuai regulasi yang diberlakukan pemerintah, yaitu selama 7 sampai dengan 8 jam bergantung jumlah hari kerja yang ditetapkan perusahaan.<br><br>

            Kelebihannya:<br>
            <li>lebih terjamin untuk menerima gaji setiap bulannya</li>
            <li>mendapatkan insentif seperti jaminan kesehatan, cuti, upah lembur, dan bentuk insentif lainnya.</li><br>

            Kekurangannya:<br>
            <li>1/3 waktu per hari terpakai untuk bekerja sehingga cukup sulit melakukan eksplorasi diri</li>
            <li>terikat dengan banyak peraturan yang diberikan perusahaan</li>
            <li>relatif monoton dalam pengerjaan proyeknya</li><br><br>

            Part Time Job Employee (Pekerja Paruh Waktu) adalah karyawan yang bekerja kurang dari 7 sampai dengan 8 jam per hari. Part time sudah umum dilakukan di negara-negara maju dimana pada perusahaan tersebut membutuhkan pergantian shift. Gaji yang diterima pun biasanya berdasarkan harian.<br><br>

            Kelebihannya:<br>
            <li>lebih fleksibel untuk memiliki lebih dari satu pekerjaan untuk menambah pengalaman baru karena waktu kerja yang relatif singkat</li>
            <li>bisa memiliki relasi yang lebih luas karena bekerja di lebih dari satu tempat</li><br>

            Kekurangannya:<br>
            <li>sistem penggajian seringkali tidak sesuai harapan karena relatif memperoleh lebih sedikit</li>
            <li>terkadang dianggap tidak profesional</li><br><br>

            Internship (Magang) adalah pelatihan kerja yang menjadi syarat kelulusan untuk mencapai gelar sarjana pada tingkat perguruan tinggi ataupun sekedar mengisi waktu luang selama waktu libur yang mana bertujuan untuk mendapat pengalaman bekerja sehingga bisa memiliki kesiapan menghadapi dunia kerja setelah lulus kuliah. Biasanya akan mendapat fee dari perusahaan yang mempekerjakan. Ada juga yang memberikan berupa sertifikat, rekomendasi pekerjaan, dan bentuk kompensasi lainnya.<br><br>

            Kelebihannya:<br>
            <li> memahami lebih awal tentang sistem bekerja di suatu perusahaan karena terjun langsung dalam mengerjakan suatu bagian proyek sehingga bisa mendapat pengalaman lebih (melengkapi teori yang didapatkan dari perkuliahan dengan cara praktek bekerja)</li>
            <li>menambah relasi rekan kerja maupun mentor</li>
            <li>berpeluang diterima sebagai pekerja tetap</li><br>

            Kekurangannya:<br>
            <li>penghasilan relatif kecil atau bahkan tidak mendapat penghasilan</li>
            <li>terkadang diremehkan karena adanya budaya senioritas</li><br><br>

            Manakah dari tipe-tipe pekerjaan diatas yang cocok dengan cara bekerja Anda? Tentukanlah mulai sekarang agar dapat melanjutkan ke langkah yang tepat.
