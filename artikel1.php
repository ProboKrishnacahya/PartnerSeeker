        <h1>Panduan Mempersiapkan Karier</h1><br><br>
        <article>Karier adalah perkembangan dalam jenjang pekerjaan seseorang yang dipengaruhi oleh rangkaian tindakan yang berkaitan dengan pengalaman dan aktivitas kerja dalam waktu tertentu.<br><br>

            Manfaat yang diperoleh dari mempersiapkan karier sedini mungkin ada banyak, yaitu sebagai berikut:<br><br>
            <li>untuk mencapai sesuatu yang didambakan</li>
            <li>dapat mempelajari hal-hal baru lebih awal</li>
            <li>memiliki lebih banyak waktu untuk mencoba sehingga dapat mempertebal pengalaman dan meminimalisir melakukan kesalahan serupa di masa mendatang</li>
            <li>terlatih untuk perencanaan jangka panjang</li>
            <li>dapat lebih mudah dalam beradaptasi</li>
            <li>membuka wawasan untuk mengenali dunia kerja lebih luas</li><br>

            Agar persiapan kita untuk menyongsong karier lebih terarah, Richard Leider telah membagikan rumus berikut:<br><br>

            <div class="rumus"><b>Karier = T + 2P + E + V</b></div> 

            <br>Sekarang, mari kita bedah maknanya satu persatu<br><br> 

            T → Talent:
            dapat melakukan breakdown kelebihan dan kelemahan diri kita untuk menyesuaikan dengan pekerjaan yang lebih sesuai dengan talenta yang dimiliki. Komponen inilah yang dapat mempercepat individu dalam proses mengasah perkembangan kemampuannya.<br><br>

            2P → Passion and Purpose:
            gairah terhadap suatu pekerjaan dapat membuat kita melakukan setiap proses yang dilalui dengan enjoy sehingga bisa meraih hasil yang maksimal. Gairah membuat kita selalu bersemangat, optimis, dan pantang menyerah. Selain gairah yang tinggi, juga diperlukan tujuan akhir terhadap perjalanan karier kita.<br><br>

            E → Environment:
            lingkungan di sekitar kita dapat berupa lingkungan keluarga, belajar, bermain. Lingkungan tentunya mempengaruhi pembentukan kepribadian masing-masing individu. Hal ini berkaitan dengan kecerdasan emosional yang mengendalikan setiap tindakan diri kita.<br><br>

            V → Vision:
            perlu menetapkan visi yang jelas dan terukur sehingga setiap progres bertahap akan lebih memotivasi kita untuk melangkah lebih jauh lagi.<br><br>

            Mari segera memulai menyusun strategi untuk masa depan.
