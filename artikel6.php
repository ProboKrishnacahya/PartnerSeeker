        <h1>Pentingnya Penggunaan Hak Cipta</h1><br><br>
        <article>Hak cipta adalah hak eksklusif bagi pencipta atau penerima hak untuk mengumumkan atau memperbanyak ciptaannya atau memberikan izin untuk itu dengan tidak mengurangi pembatasan-pembatasan menurut peraturan perundang-undangan yang berlaku. Peraturan mengenai hak cipta di Indonesia yang berlaku sampai saat ini diatur dalam Undang-undang Hak Cipta yaitu Undang-undang Nomor 28 Tahun 2014. Terdapat hak ekonomi yaitu untuk mendapatkan manfaat ekonomi dari karya yang telah diciptakan dan juga terdapat hak moral yaitu hak yang melekat pada diri pencipta yang tidak dapat dihilangkan.<br><br>

            Berikut ini kategori hasil karya yang dilindungi hak ciptanya:<br>
            <li>gambar, grafis, dan patung</li>
            <li>motion pictures</li>
            <li>drama, gerak pantomim, dan koreografi</li>
            <li>musik, audiovisual, dan audio recording</li>
            <li>tulisan</li>
            <li>karya arsitektural</li><br><br>

            Sedangkan, kategori hasil karya berikut ini tidak termasuk dilindungi hak cipta:<br>
            <li>penemuan, ide, proses, konsep, prinsip, sistem, metode, prosedur (kecuali deskripsi, ilustrasi dan sebagainya yang ditulis dan direkam yang telah terdaftar hak ciptanya)</li>
            <li>nama, judul, slogan, frasa pendek</li>
            <li>daftar bahan pembuat atau komposisi yang terkandung dalam suatu produk</li>
            <li>pertunjukan atau pidato yang diimprovisasi (tidak ditulis) dan tidak didokumentasikan serta berisi informasi umum, misalnya standar satuan ukuran, kalender standar, daftar tabel dokumen publik dan sumber umum lainnya.</li><br><br>

            Kita dapat mempelajari contoh yang termasuk lengkap yaitu yang terdapat pada <a href="https://www.youtube.com/">YouTube</a>, diantaranya<br>
            <ul>
                <li>Full Copyright: menandakan seluruh isi konten dilindungi hak cipta sehingga bagi mereka yang ingin menggunakan konten tersebut, haruslah mendapat ijin langsung dari pencipta konten.</li>
            </ul><br>
            <ul>
                <li>Creative Commons: lisensi yang diberikan oleh Organisasi non profit Creative Commons. Lisensi ini kemudian terbagi menjadi enam jenis yaitu</li>
            </ul>
            <li>Creative Commons attribution: mengijinkan orang lain untuk membuat salinan, memodifikasi, dan mendistribusikan karya. Tidak hanya itu, namun juga boleh digunakan sebagai kegiatan komersial dengan catatan tetap memberikan apresiasi kepada pencipta karya, umumnya dapat melalui penulisan nama pencipta karya aslinya.</li>
            <li>Creative Commons non-derivative: mengijinkan suatu karya untuk disalin dan didistribusikan tanpa modifikasi karya aslinya. Selain itu, juga harus tertulis kredit nama pencipta karya aslinya.</li>
            <li>Creative Commons non-commercial: mengijinkan suatu karya untuk disalin, didistribusikan, ditampilkan, membuat karya turunan yang bersifat non-komersial.</li>
            <li>Creative Commons share-alike: mengijinkan orang lain untuk mendistribusikan karya turunan selama berada dibawah naungan lisensi yang identik dengan karya aslinya.</li>
            <li>Creative Commons non-commercial-share-alike: merupakan lisensi yang mengijinkan orang lain untuk menyalin, mendistribusikan, memodifikasi, dan membuat karya turunan, namun hanya untuk tujuan non-komersial dan tetap harus mencantumkan pencipta karya aslinya.</li>
            <li>Creative Commons non-commercial-non-derivative: karya hanya dapat diunduh dan dibagikan. Saat membagikan karya, tetap harus melampirkan pemilik konten asli.</li><br>
            <ul>
                <li>Public Domain: merupakan karya milik umum yang mana masa berlaku hak ciptanya telah habis sehingga memiliki peraturan yang tidak seketat lisensi lainnya.</li>
            </ul><br><br>

            Setelah memahami tentang kegunaan hak cipta, diharapkan kita dapat lebih cermat dalam mengambil tindakan terhadap penggunaan karya-karya milik orang lain untuk mencegah terjadinya pelanggaran hak cipta sehingga tidak merugikan pihak manapun.
