        <h1>Persyaratan Wajib dalam Pembuatan Kontrak Proyek Freelance</h1><br><br>
        <article><b>1. Nama, informasi kontak, dan waktu pengesahan</b><br>
            <li>Nama Lengkap dari kedua belah pihak (freelancer dan klien) beserta topik kerja sama proyek.</li>
            <li>Kontak yang dapat dihubungi dapat berupa nomor telepon, alamat email hingga alamat geografis.</li>
            <li>Kota dan tanggal pembuatan kontrak kerja sama beserta kota dan tanggal penandatanganan sebagai tanda persetujuan oleh kedua belah pihak.</li><br>

            <div class="contoh">Contoh:<br>
                “Bob Smith (selaku penyedia layanan selanjutnya disebut sebagai Pihak Pertama) akan menyediakan jasa untuk The Foo Bar Baz Company (selaku klien selanjutnya disebut Pihak Kedua) berupa pengerjaan proyek video animasi berupa 3D Modelling untuk edukasi pencegahan stroke dan kanker sesuai detail dan spesifikasi yang terdapat pada syarat dan ketentuan berikut.<br><br>

                Perjanjian kerja sama proyek berlaku sejak 18 Mei 2021.”</div>

            <br><br><b>2. Peran freelancer dalam proyek</b><br>
            Batasan tanggung jawab pengerjaan proyek dipengaruhi oleh cakupan (lingkup) kerja sama. Perlu adanya kesepakatan terhadap job desc yang harus diselesaikan oleh freelancer sehingga tidak terjadi penuntutan dari klien terhadap pengerjaan job desc yang diluar kemampuan freelancer.<br><br>

            <div class="contoh">Contoh:<br>
                “Melayani pembuatan video 3D Modelling sebagai ilustrasi penjelasan edukasi pencegahan stroke (iskemik dan hemorragik) serta kanker (leukemia dan limfoma) dengan format .mp4 berdurasi 30 menit menggunakan aplikasi 3D Studio World dengan codec video H.264, 5.2 untuk resolusi 2160p @60 FPS tanpa audio dan tanpa subtitle untuk diunggah oleh Pihak Kedua ke kanal YouVids yang bersangkutan.<br><br>

                Runtutan konten (materi) yang perlu dibuat dalam bentuk animasi dapat dikirimkan melalui email yang dicantumkan Pihak Pertama pada kontrak kerja sama ini.”</div><br>

            Nah, jika klien meminta untuk mengerjakan pembuatan thumbnail image sebagai cover video yang diunggah di YouVids pada tanggal yang masih termasuk deadline video 3D Modelling diatas, maka freelancer berhak memutuskan untuk tidak mengerjakan permintaan tersebut karena tidak tercantum di kontrak kerja sama.<br>

            <br><br><b>3. Lamanya waktu pembuatan dan pengerjaan revisi</b><br>
            Berikan penjelasan durasi seberapa lama waktu yang diperlukan untuk menyelesaikan kebutuhan klien dan pengajuan revisi yang diperbolehkan.<br><br>

            <div class="contoh">Contoh:<br>
                “File hasil jadi dari pengerjaan video animasi dikirimkan melalui WeShare selambat-lambatnya pada 20 Oktober 2021. Lalu, untuk pemenuhan permintaan revisi dari Pihak Kedua untuk pengubahan runtutan materi tanpa penambahan durasi video hanya dapat dikerjakan paling lambat 30 hari sebelum deadline sebanyak 1 kali pengerjaan revisi.”</div>

            <br><br><b>4. Informasi pembayaran</b><br>
            Biaya (tarif) yang harus dibayarkan oleh klien beserta metode pembayaran, tanggal pembayaran, batas toleransi keterlambatan pembayaran dan konsekuensi keterlambatannya merupakan hal yang krusial sehingga haruslah tercantum dengan jelas.<br><br>

            <div class="contoh">Contoh:<br>
                “Untuk pengerjaan video animasi tersebut dikenakan biaya pengerjaan sebesar Rp7.500.000,00 (invoice dilampirkan pada tanggal yang sama dengan kontrak kerja sama ini). Pembayaran dapat dilakukan melalui transfer ke rekening Pihak Pertama Bank TabunganKu dengan 2 kali angsuran yaitu untuk angsuran pertama sebesar Rp2.500.000,00 selambat-lambatnya pada 25 Mei 2021 dan angsuran kedua sebesar Rp5.000.000,00 selambat-lambatnya pada 20 Oktober 2021.

                Batas keterlambatan hanya dapat diberikan pada angsuran pertama hingga 20 Juni 2021. Jika masih tidak dibayarkan pada tanggal tersebut, maka Pihak Pertama berhak menghentikan pengerjaan pembuatan video. Jika terjadi keterlambatan pada pembayaran kedua dengan batas hingga 25 Oktober 2021, maka pengiriman file video akan ditunda hingga pembayaran dilunasi.”</div>

            <br><br><b>5. Kepemilikan</b><br>
            Freelancer perlu memberitahukan mengenai hak-hak yang diperoleh klien dalam menggunakan hasil karya freelancer pada proyek yang telah dikerjakan. Cantumkan jenis lisensi dan ketentuan penggunaannya. Selain itu, juga harus ada kesepakatan apakah boleh menggunakan komponen yang bukan bersumber dari klien seperti font, gambar, video, suara, dan komponen penting lainnya dalam pengerjaan proyek.<br><br>

            <div class="contoh">Contoh:<br>
                “Lisensi video animasi yang dibuat oleh Pihak Pertama yaitu Creative Commons non-commercial. Pihak Pertama hanya menggunakan sumber komponen dalam video yang semuanya murni diciptakan oleh Pihak Pertama.”</div><br><br>

            Silakan membaca artikel <a href="artikel6.php"><b>“Pentingnya Penggunaan Hak Cipta”</b></a> untuk menggali informasi lebih lanjut mengenai kepemilikan karya freelancer.
